
# Declarando Conjunto - Set
numeros = set([1, 2, 3, 1, 2, 3])
letras = set("abacaxi")
carros = set(("palio","gol","celta","palio"))
linguagens = {"python", "java", "python"}

print(numeros) # {1, 2, 3}
print(letras) # {'x', 'i', 'b', 'a', 'c'}
print(carros) # {'palio', 'gol', 'celta'}
print(linguagens)
