# Função de Entrada De Dados: input
nome = input("Informe Seu Nome: ")
idade = input("Informe Sua Idade: ")

# Função de Saida de Dados: print
print(nome, idade)
print(nome, idade, end="...\n")  # Argumento end coloca String no final da função print
print(nome, idade, sep="#")  # Argumento sep coloca String na Separação da função print
print(nome, idade, sep="#", end="...\n")
