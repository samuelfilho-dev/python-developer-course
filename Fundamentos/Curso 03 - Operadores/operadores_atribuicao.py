# Declaração Simples
saldo = 500
print(saldo)

# Retribuição De Valor
saldo = 200
print(saldo)

# Adição do Valor na Atribuição
saldo += 10
print(saldo)

# Subtração do Valor na Atribuição
saldo -= 5
print(saldo)

# Divisão Inteira do Valor na Atribuição
saldo //= 2
print(saldo)

# Divisão do Valor na Atribuição
saldo /= 2
print(saldo)

# Multiplicação do Valor na Atribuição
saldo *= 10
print(saldo)

# Módulo do Valor na Atribuição
saldo %= 4
print(saldo)

# Exponenciação do Valor na Atribuição
saldo **= 2
print(saldo)

