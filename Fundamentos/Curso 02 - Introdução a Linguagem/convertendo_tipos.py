# Conversão do Tipo Float -> Inteiro
print(int(1.956575))

# Conversão do Tipo String -> Inteiro
print(int("10"))

# Conversão do Tipo String -> Float
print(float("10.10"))

# Conversão do Tipo Inteiro -> Float
print(float(100))
print(100 / 2)
print(100 // 2)

# Conversão do Tipo Float -> String
print(str(10.10))
print(type(str(10.10))) # type Verifica o Tipo da Variavel




