nome = "Samuel Marques Silva Filho"

print(nome[0]) # Primeiro Caractere
print(nome[-1]) # Ultimo Caractere

print(nome[:9]) # Criar uma sub-string a partir do nono caractere
print(nome[10:]) # Criar uma sub-string que começa do décimo caractere
print(nome[10:16]) # Criar uma sub-string que começa do décimo caractere até o décimo sexto
print(nome[10:16:2]) # Criar uma sub-string que começa do décimo caractere até o décimo sexto e passa em duas letras

print(nome[:]) # Copiar Toda String
print(nome[::-1]) # Inveter a String
